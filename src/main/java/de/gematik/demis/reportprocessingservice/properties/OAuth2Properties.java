import de.gematik.demis.reportprocessingservice.validators.EnvVariable;
import java.util.List;
import lombok.Getter;
import lombok.Setter;
import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.context.annotation.Configuration;
import org.springframework.validation.annotation.Validated;

@ConfigurationProperties(prefix = "demis.idp")
@Configuration(value = "oAuth2Properties")
@Getter
@Setter
@Validated
public class OAuth2Properties {

  /** true when the JWT validation should be performed in the service, false otherwise */
  @EnvVariable private boolean validateJwt;

  @EnvVariable
  /** the client-id */
  private String clientId;

  @EnvVariable
  /** the allowed roles */
  private List<String> allowedRoles;
}
